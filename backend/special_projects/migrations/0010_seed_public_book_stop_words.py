def normalize(value: str) -> str:
    text = unicodedata.normalize("NFKC", str(value or "")).casefold().replace("ё", "е")
    text = INVISIBLE_UNICODE_RE.sub("", text)
    text = text.translate(LATIN_TO_CYRILLIC)
    text = "".join(
        char
        for char in text
        if char.isalnum() and unicodedata.category(char) not in {"Cf", "Mn"}
    )
    return REPEATED_CHAR_RE.sub(r"\1", text)[:64]


def seed_stop_words(apps, schema_editor):
    blocked_word = apps.get_model("special_projects", "PublicBookBlockedWord")
    for word in STOP_WORDS:
        normalized_word = normalize(word)
        if not normalized_word:
            continue
        blocked_word.objects.update_or_create(
            project_slug=PROJECT_SLUG,
            normalized_word=normalized_word,
            defaults={
                "word": word,
                "is_active": True,
                "note": SOURCE_NOTE,
            },
        )


def unseed_stop_words(apps, schema_editor):
    blocked_word = apps.get_model("special_projects", "PublicBookBlockedWord")
    blocked_word.objects.filter(project_slug=PROJECT_SLUG, note=SOURCE_NOTE).delete()


